def plot_graph(G: nx.Graph, title: str, seed: int = 0, max_nodes: int = 300):
    H = G
    if H.number_of_nodes() > max_nodes:
        nodes = list(H.nodes())
        rng = np.random.default_rng(seed)
        keep = set(rng.choice(nodes, size=max_nodes, replace=False))
        H = H.subgraph(keep).copy()

    pos = nx.spring_layout(H, seed=seed)
    plt.figure(figsize=(6, 5))
    nx.draw_networkx_nodes(H, pos, node_size=10, alpha=0.8)
    nx.draw_networkx_edges(H, pos, width=0.3, alpha=0.4)
    plt.title(title)
    plt.axis("off")
    plt.show()

