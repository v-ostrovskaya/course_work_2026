def _mean_ci95(x):
    """
    Mean and approximate 95% CI using normal approximation.
    This is fine for graph-level means as a visual summary.
    """
    x = pd.Series(x).dropna().astype(float)
    n = len(x)
    mean = x.mean()
    std = x.std(ddof=1) if n > 1 else 0.0
    se = std / np.sqrt(n) if n > 1 else 0.0
    ci = 1.96 * se
    return mean, ci, n


def save_current_fig(filename):
    path = os.path.join(FIG_DIR, filename)
    plt.tight_layout()
    plt.savefig(path, dpi=300, bbox_inches="tight")
    plt.show()
    print("Saved:", path)

