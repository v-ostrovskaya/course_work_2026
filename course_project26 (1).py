def run_all_topologies(
    topologies: Dict[str, nx.Graph],
    cfg: SimConfig,
    n_runs: int = 50,
    base_seed: int = 1000,
    graph_seed: Optional[int] = None,
    graph_id: Optional[int] = None,
) -> pd.DataFrame:
    """
    Runs simulations for all topology graphs in a dictionary.
    """
    dfs = []

    for name, G in topologies.items():
        df = run_experiment(
            G=G,
            topology_name=name,
            cfg=cfg,
            n_runs=n_runs,
            base_seed=base_seed,
            graph_seed=graph_seed,
            graph_id=graph_id,
        )
        dfs.append(df)

    return pd.concat(dfs, ignore_index=True)


def run_all_topologies_over_graphs(
    n: int,
    k_target: float,
    ws_beta: float,
    cfg: SimConfig,
    graph_seeds: List[int],
    n_runs_per_graph: int,
    base_seed: int,
) -> pd.DataFrame:
    """
    Runs simulations over multiple graph realizations for each topology.

    This accounts for:
    1. graph-generation randomness;
    2. shock/agent/threshold/weight randomness.
    """
    all_dfs = []

    for g_i, g_seed in enumerate(tqdm(graph_seeds, desc="Graph realizations")):
        topologies = build_topologies(
            n=n,
            k_target=k_target,
            ws_beta=ws_beta,
            seed=g_seed,
            ensure_connected=True
        )

        df = run_all_topologies(
            topologies=topologies,
            cfg=cfg,
            n_runs=n_runs_per_graph,
            base_seed=base_seed + g_i * 10000,
            graph_seed=g_seed,
            graph_id=g_i,
        )

        all_dfs.append(df)

    return pd.concat(all_dfs, ignore_index=True)

