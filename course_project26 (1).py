def make_scenario_config(base_cfg: SimConfig, scenario: str, shock_mode: str = "random") -> SimConfig:
    """
    Creates scenario-specific configs from a common baseline.
    Scenarios:
    - baseline: no additional amplification
    - liquidity: stronger transmission, weaker recovery during crisis window
    - leverage: stressed agents transmit amplified stress
    - herding: stress increases when many neighbors are affected
    """
    cfg_dict = asdict(base_cfg)

    # Reset all scenario toggles
    cfg_dict.update({
        "shock_mode": shock_mode,
        "use_liquidity_spiral": False,
        "use_leverage_feedback": False,
        "use_herding": False,
    })

    if scenario == "baseline":
        pass

    elif scenario == "liquidity":
        cfg_dict.update({
            "use_liquidity_spiral": True,
            "liquidity_t_start": 5,
            "liquidity_t_end": 18,
            "liquidity_beta_mult": 1.25,
            "liquidity_r_mult": 0.85,
        })

    elif scenario == "leverage":
        cfg_dict.update({
            "use_leverage_feedback": True,
            "tau_mean": 0.65,
            "tau_std": 0.10,
            "leverage_lambda": 0.50,
        })

    elif scenario == "herding":
        cfg_dict.update({
            "use_herding": True,
            "phi_mean": 0.35,
            "phi_std": 0.08,
            "herding_h": 0.25,
        })

    else:
        raise ValueError(f"Unknown scenario={scenario}")

    return SimConfig(**cfg_dict)


