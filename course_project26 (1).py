# [CELL 16] Bootstrap confidence intervals for graph-level outcomes


def bootstrap_ci(samples, stat_fn=np.mean, B=2000, alpha=0.05, seed=123):
    rng = np.random.default_rng(seed)
    samples = np.asarray(samples)

    n = len(samples)

    if n == 0:
        return np.nan, np.nan

    stats = np.empty(B)

    for b in range(B):
        resample = rng.choice(samples, size=n, replace=True)
        stats[b] = stat_fn(resample)

    lo = float(np.quantile(stats, alpha / 2))
    hi = float(np.quantile(stats, 1 - alpha / 2))

    return lo, hi


def bootstrap_summary_table(
    df: pd.DataFrame,
    metric: str,
    group_cols: list,
    B: int = 2000
) -> pd.DataFrame:
    rows = []

    for key, sub in df.groupby(group_cols):
        if not isinstance(key, tuple):
            key = (key,)

        group_dict = dict(zip(group_cols, key))
        vals = sub[metric].dropna().values

        mean_val = float(np.mean(vals)) if len(vals) > 0 else np.nan
        median_val = float(np.median(vals)) if len(vals) > 0 else np.nan
        std_val = float(np.std(vals, ddof=1)) if len(vals) > 1 else np.nan

        ci_lo, ci_hi = bootstrap_ci(vals, np.mean, B=B, seed=123)

        rows.append({
            **group_dict,
            "metric": metric,
            "n": len(vals),
            "mean": mean_val,
            "median": median_val,
            "std": std_val,
            "ci95_low": ci_lo,
            "ci95_high": ci_hi,
        })

    return pd.DataFrame(rows)


