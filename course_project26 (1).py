# [CELL 17] Chi-square tests for systemic-event probabilities

from scipy.stats import chi2_contingency


def chi_square_systemic_by_condition(
    df: pd.DataFrame,
    binary_metric: str,
    condition_cols: list,
    topology_col: str = "topology"
) -> pd.DataFrame:
    rows = []

    for condition, sub in df.groupby(condition_cols):
        if not isinstance(condition, tuple):
            condition = (condition,)

        cond_dict = dict(zip(condition_cols, condition))

        contingency = pd.crosstab(sub[topology_col], sub[binary_metric])

        for col in [0, 1]:
            if col not in contingency.columns:
                contingency[col] = 0

        contingency = contingency[[0, 1]]

        if contingency.shape[0] < 2:
            continue

        try:
            chi2, p, dof, expected = chi2_contingency(contingency.values)
        except ValueError:
            chi2, p, dof = np.nan, np.nan, np.nan

        rows.append({
            **cond_dict,
            "binary_metric": binary_metric,
            "test": "Chi-square independence",
            "chi2_statistic": chi2,
            "p_value": p,
            "dof": dof,
            "significant_5pct": bool(p < 0.05) if not pd.isna(p) else False,
            "contingency_table": contingency.to_dict(),
        })

    return pd.DataFrame(rows)


