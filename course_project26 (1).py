# [CELL 3] Graph generators (ER / WS / BA / CMP) and target average degree matching

def make_er(n: int, k_target: float, seed: int) -> nx.Graph:
    # For ER: expected avg degree ≈ p*(n-1) => p ≈ k/(n-1)
    p = float(k_target) / max(n - 1, 1)
    p = min(max(p, 0.0), 1.0)
    G = nx.erdos_renyi_graph(n=n, p=p, seed=seed)
    return G

def make_ws(n: int, k_target: int, beta: float, seed: int) -> nx.Graph:
    # For WS: k must be even and satisfy 0 < k < n
    if n < 3:
        raise ValueError("Watts-Strogatz graph requires n >= 3")

    k = int(round(k_target))

    # k must be less than n
    k = min(k, n - 1)

    # k must be even
    if k % 2 == 1:
        k -= 1

    # keep >= 2 neighbors if possible
    k = max(2, k)

    # if n is very small and k == n, reduce again
    if k >= n:
        k = n - 1
        if k % 2 == 1:
            k -= 1

    if k < 2:
        raise ValueError(f"Invalid WS k={k} for n={n}")

    return nx.watts_strogatz_graph(n=n, k=k, p=beta, seed=seed)

def make_ba(n: int, k_target: float, seed: int) -> nx.Graph:
    # For BA: avg degree ≈ 2m  => m ≈ k/2
    m = int(max(1, round(k_target / 2)))
    m = min(m, n - 1)
    G = nx.barabasi_albert_graph(n=n, m=m, seed=seed)
    return G
def expected_avg_degree_block_model(n: int, sizes: List[int], P: np.ndarray) -> float:
    """
    Expected average degree for an undirected stochastic block model.
    """
    expected_edges = 0.0
    B = len(sizes)

    for a in range(B):
        for b in range(a, B):
            if a == b:
                possible_edges = sizes[a] * (sizes[a] - 1) / 2
            else:
                possible_edges = sizes[a] * sizes[b]

            expected_edges += possible_edges * P[a, b]

    return 2.0 * expected_edges / n


def calibrate_block_probabilities(
    n: int,
    sizes: List[int],
    base_affinity: np.ndarray,
    k_target: float,
    p_max: float = 0.95,
    n_iter: int = 60
) -> np.ndarray:
    """
    Scales a base affinity matrix so that expected average degree
    is approximately equal to k_target.
    """
    base_affinity = np.asarray(base_affinity, dtype=float)

    lo, hi = 0.0, 1.0

    # Increase upper bound until expected degree is high enough
    for _ in range(50):
        P_hi = np.minimum(hi * base_affinity, p_max)
        k_hi = expected_avg_degree_block_model(n, sizes, P_hi)
        if k_hi >= k_target:
            break
        hi *= 2.0

    # Binary search for scale
    for _ in range(n_iter):
        mid = (lo + hi) / 2.0
        P_mid = np.minimum(mid * base_affinity, p_max)
        k_mid = expected_avg_degree_block_model(n, sizes, P_mid)

        if k_mid < k_target:
            lo = mid
        else:
            hi = mid

    return np.minimum(hi * base_affinity, p_max)


def ensure_connected_by_bridges(G: nx.Graph, seed: int) -> nx.Graph:
    """
    Keeps all nodes and connects disconnected components by adding bridges.
    This preserves N across all topologies.
    """
    if nx.is_connected(G):
        return G

    rng = np.random.default_rng(seed)
    components = [list(c) for c in nx.connected_components(G)]
    components = sorted(components, key=len, reverse=True)

    main = components[0]

    for comp in components[1:]:
        u = int(rng.choice(main))
        v = int(rng.choice(comp))
        G.add_edge(u, v)
        main.extend(comp)

    return G

def make_core_middle_periphery(
    n: int,
    k_target: float,
    seed: int,
    core_frac: float = 0.10,
    middle_frac: float = 0.25
) -> nx.Graph:
    """
    Stylized core-middle-periphery topology.

    For N=300 by default:
    - core ≈ 30 nodes
    - middle ≈ 75 nodes
    - periphery ≈ 195 nodes
    """
    rng = np.random.default_rng(seed)

    n_core = int(round(core_frac * n))
    n_middle = int(round(middle_frac * n))
    n_periphery = n - n_core - n_middle

    n_core = max(2, n_core)
    n_middle = max(2, n_middle)
    n_periphery = max(2, n_periphery)

    total = n_core + n_middle + n_periphery
    if total != n:
        n_periphery += n - total

    sizes = [n_core, n_middle, n_periphery]

    base_affinity = np.array([
    [1.00, 0.50, 0.05],
    [0.50, 0.22, 0.12],
    [0.05, 0.12, 0.01],
])

    P = calibrate_block_probabilities(
        n=n,
        sizes=sizes,
        base_affinity=base_affinity,
        k_target=k_target,
        p_max=0.95
    )

    G = nx.stochastic_block_model(
        sizes=sizes,
        p=P,
        seed=seed,
        directed=False,
        selfloops=False
    )

    layer_names = ["core", "middle", "periphery"]
    node_layer = {}
    start = 0

    for layer_name, size in zip(layer_names, sizes):
        for node in range(start, start + size):
            node_layer[node] = layer_name
        start += size

    nx.set_node_attributes(G, node_layer, "layer")

    G = ensure_connected_by_bridges(G, seed=seed)

    G.graph["block_sizes"] = {
        "core": n_core,
        "middle": n_middle,
        "periphery": n_periphery
    }
    G.graph["block_probabilities"] = P

    return G

def ensure_largest_component(G: nx.Graph) -> nx.Graph:
    if nx.is_connected(G):
        return G
    comp = max(nx.connected_components(G), key=len)
    return G.subgraph(comp).copy()

def graph_summary(G: nx.Graph) -> Dict[str, Any]:
    n = G.number_of_nodes()
    m = G.number_of_edges()
    degs = np.array([d for _, d in G.degree()])
    avg_deg = float(degs.mean()) if n > 0 else 0.0
    density = nx.density(G) if n > 1 else 0.0
    clustering = nx.average_clustering(G) if n > 0 else 0.0

    # Avg shortest path length only for connected graphs (or use largest component)
    if n > 1 and nx.is_connected(G):
        aspl = nx.average_shortest_path_length(G)
    else:
        aspl = np.nan

    return {
        "n": n,
        "m": m,
        "avg_deg": avg_deg,
        "density": density,
        "clustering": clustering,
        "avg_shortest_path_len": aspl,
        "deg_min": int(degs.min()) if n > 0 else 0,
        "deg_max": int(degs.max()) if n > 0 else 0,
        "deg_p90": float(np.percentile(degs, 90)) if n > 0 else 0.0,
    }

def build_topologies(
    n: int,
    k_target: float,
    ws_beta: float,
    seed: int,
    ensure_connected: bool = True
) -> Dict[str, nx.Graph]:
    G_er = make_er(n, k_target, seed=seed)
    G_ws = make_ws(n, k_target=int(round(k_target)), beta=ws_beta, seed=seed)
    G_ba = make_ba(n, k_target, seed=seed)
    G_cmp = make_core_middle_periphery(n=n, k_target=k_target, seed=seed)

    if ensure_connected:
        G_er = ensure_connected_by_bridges(G_er, seed=seed + 1)
        G_ws = ensure_connected_by_bridges(G_ws, seed=seed + 2)
        G_ba = ensure_connected_by_bridges(G_ba, seed=seed + 3)
        G_cmp = ensure_connected_by_bridges(G_cmp, seed=seed + 4)

    return {
        "ER": G_er,
        "WS": G_ws,
        "BA": G_ba,
        "CMP": G_cmp
    }

