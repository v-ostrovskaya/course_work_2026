@dataclass
class SimConfig:
    # Network / simulation
    # T - the maximum simulation horizon
    # If stop_when_stable=True, the simulation stops earlier when aggregate dynamics become stable.
    T: int = 100
    stop_when_stable: bool = True
    stability_window: int = 5
    stability_eps: float = 1e-4
    min_steps: int = 10

    shock_fraction: float = 0.05       # fraction of nodes shocked at t=0
    shock_mode: str = "random"         # "random" or "targeted"
    shock_strength: float = 0.35       # stress added at t=0
    initial_shock_state: str = "stress"  # "stress", "distress", or "default"

    # Stress update core
    r: float = 0.08                    # recovery rate (0..1)
    beta: float = 0.25                 # transmission strength
    normalize_incoming: bool = True    # divide incoming stress by node degree
    clip_min: float = 0.0
    clip_max: float = 1.0

    # Thresholds
    theta_distress_mean: float = 0.55
    theta_distress_std: float = 0.08
    theta_default_mean: float = 0.85
    theta_default_std: float = 0.05

    # Systemic-event thresholds for output metrics
    systemic_default_threshold: float = 0.30   # systemic event if final_default >= 30%
    systemic_cascade_threshold: float = 0.50   # systemic event if cascade_size >= 50%

    # Scenario toggles
    use_liquidity_spiral: bool = False
    use_leverage_feedback: bool = False
    use_herding: bool = False

    # Liquidity spiral parameters
    liquidity_t_start: int = 5
    liquidity_t_end: int = 18
    liquidity_beta_mult: float = 1.35  # beta increases during liquidity stress
    liquidity_r_mult: float = 0.75     # recovery becomes weaker during liquidity stress

    # Leverage feedback parameters
    tau_mean: float = 0.65
    tau_std: float = 0.10
    leverage_lambda: float = 0.8       # outgoing stress multiplier if s_i > tau_i

    # Herding parameters
    phi_mean: float = 0.35             # threshold of distressed/defaulted-neighbor share
    phi_std: float = 0.08
    herding_h: float = 0.40            # additive stress: h*(q_i - phi_i)+

    # Edge weights
    directed_weights: bool = False     # if True: random directed weights per edge
    weight_scale: float = 1.0


def _draw_normal_clip(
    rng: np.random.Generator,
    mean: float,
    std: float,
    size: int,
    lo: float = 0.0,
    hi: float = 1.0
):
    x = rng.normal(mean, std, size=size)
    return np.clip(x, lo, hi)


def pick_shocked_nodes(G: nx.Graph, frac: float, mode: str, seed: int) -> List[int]:
    rng = np.random.default_rng(seed)
    nodes = list(G.nodes())
    k = int(max(1, round(frac * len(nodes))))

    if mode == "random":
        return list(rng.choice(nodes, size=k, replace=False))

    if mode == "targeted":
        # Target structurally central agents: highest-degree nodes.
        deg = dict(G.degree())
        nodes_sorted = sorted(nodes, key=lambda x: deg[x], reverse=True)
        return nodes_sorted[:k]

    raise ValueError(f"Unknown shock_mode={mode}")

# [CELL 6] Core simulation: stress propagation + scenarios + stabilization stopping

# 3 possible states: Ok, Distressed (recovery is still possible), Default
STATE_OK = 0
STATE_DISTRESSED = 1
STATE_DEFAULTED = 2


def simulate_once(G: nx.Graph, cfg: SimConfig, seed: int) -> Dict[str, Any]:
    """
    Stylized threshold-based ABM of systemic-risk propagation.

    Interpretation:
    - s_i(t): normalized stress level of agent i
    - DISTRESSED: stress exceeds individual distress threshold
    - DEFAULTED: stress exceeds individual default threshold
    - cascade_size: fraction of nodes that ever become DISTRESSED or DEFAULTED

    The simulation runs until either:
    1. aggregate dynamics stabilize, if cfg.stop_when_stable=True; or
    2. the maximum horizon cfg.T is reached.

    Therefore, cfg.T is a maximum simulation horizon, not as
    the actual crisis duration.
    """
    rng = np.random.default_rng(seed)

    nodes = list(G.nodes())
    idx = {u: i for i, u in enumerate(nodes)}
    n = len(nodes)

    # 1. Node-specific thresholds
    theta_d = _draw_normal_clip(
        rng,
        cfg.theta_distress_mean,
        cfg.theta_distress_std,
        n,
        lo=0.05,
        hi=0.99
    )

    theta_f = _draw_normal_clip(
        rng,
        cfg.theta_default_mean,
        cfg.theta_default_std,
        n,
        lo=0.10,
        hi=0.999
    )

    # Ensure default threshold is always above distress threshold
    theta_f = np.maximum(theta_f, theta_d + 0.05)
    theta_f = np.clip(theta_f, 0.10, 0.999)

    # Leverage thresholds tau_i
    tau = _draw_normal_clip(
        rng,
        cfg.tau_mean,
        cfg.tau_std,
        n,
        lo=0.05,
        hi=0.99
    )

    # Herding thresholds phi_i
    phi = _draw_normal_clip(
        rng,
        cfg.phi_mean,
        cfg.phi_std,
        n,
        lo=0.01,
        hi=0.99
    )

    # 2. Weighted neighbor lists
    if cfg.directed_weights:
        # in_neighbors[i] contains (j, w_ji), meaning stress flows from j to i
        in_neighbors = [[] for _ in range(n)]

        for u, v in G.edges():
            w_uv = float(rng.random() * cfg.weight_scale)
            w_vu = float(rng.random() * cfg.weight_scale)

            iu, iv = idx[u], idx[v]

            in_neighbors[iu].append((iv, w_vu))  # v -> u
            in_neighbors[iv].append((iu, w_uv))  # u -> v

    else:
        # Undirected stress-transmission weights
        in_neighbors = [[] for _ in range(n)]

        for u, v in G.edges():
            w_uv = float(rng.random() * cfg.weight_scale)

            iu, iv = idx[u], idx[v]

            in_neighbors[iu].append((iv, w_uv))
            in_neighbors[iv].append((iu, w_uv))

    # 3. Initial conditions

    s = np.zeros(n, dtype=float)
    state = np.zeros(n, dtype=int)  # all OK

    shocked = pick_shocked_nodes(
        G,
        frac=cfg.shock_fraction,
        mode=cfg.shock_mode,
        seed=seed
    )
    shocked_idx = [idx[u] for u in shocked]

    # Add initial stress
    s[shocked_idx] = np.clip(
        s[shocked_idx] + cfg.shock_strength,
        cfg.clip_min,
        cfg.clip_max
    )

    # Interpret initial shock
    if cfg.initial_shock_state == "distress":
        for ii in shocked_idx:
            # Push shocked agents into distress, but don't force immediate default
            s[ii] = max(s[ii], theta_d[ii])
            s[ii] = min(s[ii], theta_f[ii] - 1e-6)
            state[ii] = STATE_DISTRESSED

    elif cfg.initial_shock_state == "default":
        for ii in shocked_idx:
            s[ii] = max(s[ii], theta_f[ii])
            state[ii] = STATE_DEFAULTED

    elif cfg.initial_shock_state == "stress":
        state[s >= theta_d] = STATE_DISTRESSED
        state[s >= theta_f] = STATE_DEFAULTED

    else:
        raise ValueError(f"Unknown initial_shock_state={cfg.initial_shock_state}")

    # Track ever-affected nodes
    ever_affected = (state == STATE_DISTRESSED) | (state == STATE_DEFAULTED)
    ever_defaulted = (state == STATE_DEFAULTED)

    # 4. Time series containers

    ts_frac_distressed = []
    ts_frac_defaulted = []
    ts_frac_affected = []
    ts_avg_stress = []

    def record_state():
        ts_frac_distressed.append(float((state == STATE_DISTRESSED).mean()))
        ts_frac_defaulted.append(float((state == STATE_DEFAULTED).mean()))
        ts_frac_affected.append(
            float(((state == STATE_DISTRESSED) | (state == STATE_DEFAULTED)).mean())
        )
        ts_avg_stress.append(float(s.mean()))

    record_state()

    # 5. Main simulation loop

    stopped_early = False
    stop_time = cfg.T

    for t in range(cfg.T):
        beta_t = cfg.beta
        r_t = cfg.r

        # Liquidity stress: stronger transmission and weaker recovery
        if cfg.use_liquidity_spiral and (cfg.liquidity_t_start <= t <= cfg.liquidity_t_end):
            beta_t *= cfg.liquidity_beta_mult
            r_t *= cfg.liquidity_r_mult

        s_new = s.copy()

        distressed_mask = (state == STATE_DISTRESSED)
        defaulted_mask = (state == STATE_DEFAULTED)
        affected_mask = distressed_mask | defaulted_mask

        for i in range(n):
            if state[i] == STATE_DEFAULTED:
                # Default is absorbing
                s_new[i] = max(s_new[i], theta_f[i])
                continue

            incoming = 0.0

            for j, w_ji in in_neighbors[i]:
                # Stress contribution from neighbor j
                if defaulted_mask[j]:
                    g = 1.0
                elif distressed_mask[j]:
                    g = min(1.0, s[j] * 1.15)
                else:
                    g = s[j]

                # Leverage / margin feedback:
                # if neighbor j is above its leverage threshold, it transmits more stress
                if cfg.use_leverage_feedback and s[j] > tau[j]:
                    g *= (1.0 + cfg.leverage_lambda)

                incoming += w_ji * g

            deg_i = max(1, len(in_neighbors[i]))

            # Normalize incoming stress by degree in the baseline model
            if cfg.normalize_incoming:
                incoming /= deg_i

            # Core stress update
            s_new[i] = (1.0 - r_t) * s[i] + beta_t * incoming

            # Herding:
            # stress increases if the share of affected neighbors exceeds phi_i
            if cfg.use_herding:
                if deg_i > 0:
                    neighbor_ids = [j for j, _ in in_neighbors[i]]
                    q_i = float(affected_mask[neighbor_ids].mean())
                else:
                    q_i = 0.0

                if q_i >= phi[i]:
                    s_new[i] += cfg.herding_h * (q_i - phi[i])

            s_new[i] = float(np.clip(s_new[i], cfg.clip_min, cfg.clip_max))

        s = s_new

        # 6. State update

        new_state = state.copy()

        # Default is absorbing
        new_state[s >= theta_f] = STATE_DEFAULTED

        # Distress condition for non-defaulted nodes
        distressed_now = (s >= theta_d) & (new_state != STATE_DEFAULTED)

        # Recovery is allowed from DISTRESSED to OK, but not from DEFAULTED
        new_state[(~distressed_now) & (new_state != STATE_DEFAULTED)] = STATE_OK
        new_state[distressed_now] = STATE_DISTRESSED

        state = new_state

        # Update cascade tracking
        ever_affected |= (state == STATE_DISTRESSED) | (state == STATE_DEFAULTED)
        ever_defaulted |= (state == STATE_DEFAULTED)

        record_state()

      # 7. Optional early stopping

        min_stop_step = cfg.min_steps

        if cfg.use_liquidity_spiral:
            min_stop_step = max(
                min_stop_step,
                cfg.liquidity_t_end + cfg.stability_window + 1
            )

        if cfg.stop_when_stable and (t + 1) >= min_stop_step:
            window = int(cfg.stability_window)

            if len(ts_frac_affected) > window + 1:
                affected_recent = np.array(ts_frac_affected[-(window + 1):])
                default_recent = np.array(ts_frac_defaulted[-(window + 1):])
                stress_recent = np.array(ts_avg_stress[-(window + 1):])

                max_change_affected = float(np.max(np.abs(np.diff(affected_recent))))
                max_change_default = float(np.max(np.abs(np.diff(default_recent))))
                max_change_stress = float(np.max(np.abs(np.diff(stress_recent))))

                if (
                    max_change_affected <= cfg.stability_eps
                    and max_change_default <= cfg.stability_eps
                    and max_change_stress <= cfg.stability_eps
                ):
                    stopped_early = True
                    stop_time = t + 1
                    break

    # 8. Summary metrics

    cascade_size = float(ever_affected.mean())  # main cascade-size metric
    ever_defaulted_fraction = float(ever_defaulted.mean())

    final_affected = float(((state == STATE_DISTRESSED) | (state == STATE_DEFAULTED)).mean())
    final_default = float((state == STATE_DEFAULTED).mean())

    peak_default = float(np.max(ts_frac_defaulted))
    peak_distress = float(np.max(ts_frac_distressed))
    peak_affected = float(np.max(ts_frac_affected))

    default_arr = np.array(ts_frac_defaulted)
    affected_arr = np.array(ts_frac_affected)

    default_changes = np.where(np.abs(np.diff(default_arr)) > 1e-9)[0]
    affected_changes = np.where(np.abs(np.diff(affected_arr)) > 1e-9)[0]

    # Old duration-like metrics: last time when the fraction changed
    # These are kept for comparability, but they can be close to the horizon
    # when small changes continue until the end.
    # cascade_duration_default = int(default_changes[-1] + 1) if len(default_changes) > 0 else 0
    # cascade_duration_affected = int(affected_changes[-1] + 1) if len(affected_changes) > 0 else 0

    time_to_peak_default = int(np.argmax(default_arr))
    time_to_peak_affected = int(np.argmax(affected_arr))

    def first_time_reaching_fraction_of_peak(arr: np.ndarray, frac: float) -> int:
        peak = float(np.max(arr))
        if peak <= 1e-12:
            return 0

        target = frac * peak
        hits = np.where(arr >= target)[0]

        if len(hits) == 0:
            return 0

        return int(hits[0])

    time_to_50pct_peak_affected = first_time_reaching_fraction_of_peak(affected_arr, 0.50)
    time_to_90pct_peak_affected = first_time_reaching_fraction_of_peak(affected_arr, 0.90)

    time_to_50pct_peak_default = first_time_reaching_fraction_of_peak(default_arr, 0.50)
    time_to_90pct_peak_default = first_time_reaching_fraction_of_peak(default_arr, 0.90)

    active_cascade_duration_affected = max(
        0,
        time_to_90pct_peak_affected - time_to_50pct_peak_affected
    )

    active_cascade_duration_default = max(
        0,
        time_to_90pct_peak_default - time_to_50pct_peak_default
    )

    # Area under the curve: total burden of distress/default over time
    area_affected = float(np.trapezoid(affected_arr))
    area_default = float(np.trapezoid(default_arr))

    # Stabilization time:
    # first time after which changes remain smaller than eps until the end.
    def stabilization_time(arr: np.ndarray, eps: float = 1e-4) -> int:
        diffs = np.abs(np.diff(arr))

        if len(diffs) == 0:
            return 0

        for tt in range(len(diffs)):
            if np.all(diffs[tt:] <= eps):
                return int(tt)

        return int(len(arr) - 1)

    stabilization_time_affected = stabilization_time(affected_arr, eps=cfg.stability_eps)
    stabilization_time_default = stabilization_time(default_arr, eps=cfg.stability_eps)

    systemic_by_default = int(final_default >= cfg.systemic_default_threshold)
    systemic_by_cascade = int(cascade_size >= cfg.systemic_cascade_threshold)

    return {
        "seed": seed,
        "n": n,

        # Main outcomes
        "cascade_size": cascade_size,
        "final_default": final_default,
        "final_affected": final_affected,
        "ever_defaulted": ever_defaulted_fraction,

        # Peak outcomes
        "peak_default": peak_default,
        "peak_distress": peak_distress,
        "peak_affected": peak_affected,

        # Systemic-event flags
        "systemic_by_default": systemic_by_default,
        "systemic_by_cascade": systemic_by_cascade,

        # Timing outcomes
        # "cascade_duration_default": cascade_duration_default,
        # "cascade_duration_affected": cascade_duration_affected,
        "time_to_peak_default": time_to_peak_default,
        "time_to_peak_affected": time_to_peak_affected,

        "time_to_50pct_peak_affected": time_to_50pct_peak_affected,
        "time_to_90pct_peak_affected": time_to_90pct_peak_affected,
        "time_to_50pct_peak_default": time_to_50pct_peak_default,
        "time_to_90pct_peak_default": time_to_90pct_peak_default,

        "active_cascade_duration_affected": active_cascade_duration_affected,
        "active_cascade_duration_default": active_cascade_duration_default,

        "stabilization_time_affected": stabilization_time_affected,
        "stabilization_time_default": stabilization_time_default,

        "area_affected": area_affected,
        "area_default": area_default,

        # Stopping information
        "stopped_early": int(stopped_early),
        "stop_time": int(stop_time),
        "max_T": int(cfg.T),

        # Time series
        "ts_frac_distressed": ts_frac_distressed,
        "ts_frac_defaulted": ts_frac_defaulted,
        "ts_frac_affected": ts_frac_affected,
        "ts_avg_stress": ts_avg_stress,

        # Initial shock
        "shocked_count": len(shocked_idx),
    }

