# [CELL 15] Effect sizes: Cliff's delta for pairwise topology differences

from itertools import combinations

def cliffs_delta(x, y):
    """
    Cliff's delta:
    P(x > y) - P(x < y).
    Values close to 0 mean weak difference.
    Values close to +/-1 mean strong difference.
    """
    x = np.asarray(x)
    y = np.asarray(y)

    n_x = len(x)
    n_y = len(y)

    if n_x == 0 or n_y == 0:
        return np.nan

    greater = 0
    less = 0

    for xi in x:
        greater += np.sum(xi > y)
        less += np.sum(xi < y)

    return (greater - less) / (n_x * n_y)


def cliffs_delta_label(delta):
    """
    Common rough interpretation thresholds.
    """
    if pd.isna(delta):
        return "NA"

    ad = abs(delta)

    if ad < 0.147:
        return "negligible"
    elif ad < 0.33:
        return "small"
    elif ad < 0.474:
        return "medium"
    else:
        return "large"


def pairwise_effect_sizes_by_condition(
    df: pd.DataFrame,
    metric: str,
    condition_cols: list,
    topology_col: str = "topology"
) -> pd.DataFrame:
    rows = []

    for condition, sub in df.groupby(condition_cols):
        if not isinstance(condition, tuple):
            condition = (condition,)

        cond_dict = dict(zip(condition_cols, condition))

        topologies = sorted(sub[topology_col].dropna().unique())

        for topo_a, topo_b in combinations(topologies, 2):
            x = sub[sub[topology_col] == topo_a][metric].dropna().values
            y = sub[sub[topology_col] == topo_b][metric].dropna().values

            delta = cliffs_delta(x, y)

            rows.append({
                **cond_dict,
                "metric": metric,
                "topology_1": topo_a,
                "topology_2": topo_b,
                "cliffs_delta": delta,
                "effect_size_label": cliffs_delta_label(delta),
                "mean_1": float(np.mean(x)) if len(x) > 0 else np.nan,
                "mean_2": float(np.mean(y)) if len(y) > 0 else np.nan,
                "median_1": float(np.median(x)) if len(x) > 0 else np.nan,
                "median_2": float(np.median(y)) if len(y) > 0 else np.nan,
                "n_1": len(x),
                "n_2": len(y),
            })

    return pd.DataFrame(rows)


