# [CELL 12] Statistical tests: Kruskal-Wallis + Mann-Whitney U

def holm_correction(p_values: List[float]) -> List[float]:
    """
    Holm-Bonferroni correction for multiple comparisons.
    Returns adjusted p-values in the original order.
    """
    p_values = np.array(p_values, dtype=float)
    m = len(p_values)

    if m == 0:
        return []

    order = np.argsort(p_values)
    adjusted = np.empty(m, dtype=float)

    prev = 0.0

    for rank, idx in enumerate(order):
        adj = (m - rank) * p_values[idx]
        adj = max(adj, prev)
        adjusted[idx] = min(adj, 1.0)
        prev = adjusted[idx]

    return adjusted.tolist()


def run_tests_by_condition(
    df: pd.DataFrame,
    metric: str,
    condition_cols: List[str],
    topology_col: str = "topology"
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """
    For each condition:
    1. Kruskal-Wallis test across all topologies.
    2. Pairwise Mann-Whitney U tests with Holm correction.

    Intended use in this notebook:
    run this on graph-level aggregated outcomes, not on raw simulation-level rows.
    """
    kw_rows = []
    pair_rows = []

    for condition, sub in df.groupby(condition_cols):
        if not isinstance(condition, tuple):
            condition = (condition,)

        cond_dict = dict(zip(condition_cols, condition))

        labels = []
        groups = []

        for topo, g in sub.groupby(topology_col):
            vals = g[metric].dropna().values

            if len(vals) > 1:
                labels.append(topo)
                groups.append(vals)

        if len(groups) < 2:
            continue

        stat, p = kruskal(*groups)

        kw_rows.append({
            **cond_dict,
            "metric": metric,
            "test": "Kruskal-Wallis",
            "statistic": stat,
            "p_value": p,
            "significant_5pct": p < 0.05,
            "n_groups": len(groups),
        })

        raw_p = []
        temp_rows = []

        for topo_a, topo_b in combinations(labels, 2):
            x = sub[sub[topology_col] == topo_a][metric].dropna().values
            y = sub[sub[topology_col] == topo_b][metric].dropna().values

            if len(x) == 0 or len(y) == 0:
                continue

            u_stat, p_pair = mannwhitneyu(x, y, alternative="two-sided")

            temp_rows.append({
                **cond_dict,
                "metric": metric,
                "topology_1": topo_a,
                "topology_2": topo_b,
                "test": "Mann-Whitney U",
                "statistic": u_stat,
                "p_value_raw": p_pair,
            })

            raw_p.append(p_pair)

        adjusted_p = holm_correction(raw_p)

        for row, p_adj in zip(temp_rows, adjusted_p):
            row["p_value_holm"] = p_adj
            row["significant_5pct"] = p_adj < 0.05
            pair_rows.append(row)

