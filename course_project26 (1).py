def plot_grouped_bar_ci(
    df_graph,
    group_col,
    group_order,
    metric,
    title,
    ylabel,
    filename,
    topology_order=TOPOLOGY_ORDER,
):
    rows = []

    for group in group_order:
        for topo in topology_order:
            vals = df_graph[
                (df_graph[group_col] == group) &
                (df_graph["topology"] == topo)
            ][metric]

            mean, ci, n = _mean_ci95(vals)

            rows.append({
                group_col: group,
                "topology": topo,
                "mean": mean,
                "ci95": ci,
                "n": n,
            })

    summary = pd.DataFrame(rows)

    x = np.arange(len(group_order))
    width = 0.18

    plt.figure(figsize=(9.5, 5.2))

    for i, topo in enumerate(topology_order):
        sub = summary[summary["topology"] == topo]
        means = sub["mean"].values
        cis = sub["ci95"].values

        plt.bar(
            x + (i - 1.5) * width,
            means,
            width=width,
            yerr=cis,
            capsize=3,
            label=topo,
            color=TOPOLOGY_COLORS[topo],
            alpha=0.9,
            edgecolor="black",
            linewidth=0.4,
        )

    plt.xticks(x, group_order)
    plt.ylabel(ylabel)
    plt.title(title)
    plt.legend(title="Topology", frameon=True)
    plt.grid(axis="y", alpha=0.25)

    save_current_fig(filename)


